class Solution {
  public:
    int canReach(int A[], int N) {
        // code here
        int maxind=0;
        for(int i=0;i<N;i++){
            if(i>maxind) return 0;
            
            maxind=max(maxind, i+A[i]);
        }
        return 1;
    }
};

